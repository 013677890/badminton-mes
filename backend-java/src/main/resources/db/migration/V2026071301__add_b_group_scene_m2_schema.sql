    product_name varchar(128) NOT NULL COMMENT '产品名称快照',
    batch_no varchar(64) NOT NULL COMMENT '批次号',
    routing_id bigint unsigned NOT NULL COMMENT '工艺路线',
    routing_code varchar(32) NOT NULL COMMENT '路线编码快照',
    routing_version varchar(16) NOT NULL COMMENT '路线版本快照',
    workshop_id bigint unsigned NOT NULL COMMENT '车间',
    workshop_name varchar(128) NOT NULL COMMENT '车间名称快照',
    line_id bigint unsigned NOT NULL COMMENT '产线',
    line_name varchar(128) NOT NULL COMMENT '产线名称快照',
    shift_id bigint unsigned NULL COMMENT '班次',
    plan_date date NOT NULL COMMENT '计划日期',
    plan_quantity int unsigned NOT NULL COMMENT '计划数量',
    input_quantity int unsigned NOT NULL DEFAULT 0 COMMENT '投入数量',
    good_quantity int unsigned NOT NULL DEFAULT 0 COMMENT '良品数量',
    defect_quantity int unsigned NOT NULL DEFAULT 0 COMMENT '不良数量',
    rework_quantity int unsigned NOT NULL DEFAULT 0 COMMENT '返修数量',
    finish_quantity int unsigned NOT NULL DEFAULT 0 COMMENT '完工数量',
    plan_start_time datetime NOT NULL COMMENT '计划开始时间',
    plan_end_time datetime NOT NULL COMMENT '计划结束时间',
    actual_start_time datetime NULL COMMENT '实际开始时间',
    actual_end_time datetime NULL COMMENT '实际结束时间',
    task_status tinyint unsigned NOT NULL DEFAULT 0 COMMENT '0待审核 1已审核 2已下发 3生产中 4暂停 5已完工 6已关闭 7已取消',
    pause_reason varchar(255) NULL COMMENT '暂停或关闭原因',
    audit_by bigint unsigned NULL COMMENT '审核人',
    audit_time datetime NULL COMMENT '审核时间',
    create_by bigint unsigned NOT NULL COMMENT '创建人',
    create_time datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    is_deleted tinyint unsigned NOT NULL DEFAULT 0 COMMENT '逻辑删除',
    PRIMARY KEY (id),
    UNIQUE KEY uk_task_no (task_no),
    KEY idx_work_order_status (work_order_id, task_status),
    KEY idx_line_date_status (line_id, plan_date, task_status),
    KEY idx_batch_no (batch_no(20))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='生产任务单';

