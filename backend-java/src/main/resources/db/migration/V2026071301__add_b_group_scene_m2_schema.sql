CREATE TABLE IF NOT EXISTS base_production_line (
    id bigint unsigned NOT NULL AUTO_INCREMENT COMMENT '主键',
    line_code varchar(32) NOT NULL COMMENT '产线编码',
    line_name varchar(64) NOT NULL COMMENT '产线名称',
    workshop_id bigint unsigned NOT NULL COMMENT '所属车间',
    standard_capacity int unsigned NULL COMMENT '标准日产能',
    status tinyint unsigned NOT NULL DEFAULT 1 COMMENT '状态',
    create_time datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    is_deleted tinyint unsigned NOT NULL DEFAULT 0 COMMENT '逻辑删除',
    PRIMARY KEY (id),
    UNIQUE KEY uk_line_code (line_code),
    KEY idx_workshop_id (workshop_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='产线表';

CREATE TABLE prod_param (
    id bigint unsigned NOT NULL AUTO_INCREMENT COMMENT '主键',
    param_code varchar(64) NOT NULL COMMENT '参数编码',
    param_name varchar(128) NOT NULL COMMENT '参数名称',
    param_value varchar(255) NOT NULL COMMENT '参数值',
    value_type tinyint unsigned NOT NULL DEFAULT 1 COMMENT '值类型:1开关 2数量 3枚举 4文本',
    workshop_id bigint unsigned NULL COMMENT '适用车间',
    line_id bigint unsigned NULL COMMENT '适用产线',
    product_id bigint unsigned NULL COMMENT '适用产品',
    scope_workshop_id bigint unsigned GENERATED ALWAYS AS (IFNULL(workshop_id, 0)) STORED,
    scope_line_id bigint unsigned GENERATED ALWAYS AS (IFNULL(line_id, 0)) STORED,
    scope_product_id bigint unsigned GENERATED ALWAYS AS (IFNULL(product_id, 0)) STORED,
    remark varchar(255) NULL COMMENT '参数说明',
    status tinyint unsigned NOT NULL DEFAULT 1 COMMENT '状态:1启用 0停用',
    create_by bigint unsigned NOT NULL COMMENT '创建人',
    create_time datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    is_deleted tinyint unsigned NOT NULL DEFAULT 0 COMMENT '逻辑删除',
    PRIMARY KEY (id),
    UNIQUE KEY uk_code_scope (param_code, scope_workshop_id, scope_line_id, scope_product_id),
    KEY idx_effective_scope (param_code, status, workshop_id, line_id, product_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='生产参数配置表';

